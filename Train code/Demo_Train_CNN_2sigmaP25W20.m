rng('default')

addpath('utilities');
%-------------------------------------------------------------------------
% Configuration
%-------------------------------------------------------------------------
opts.modelName        = 'DnCNN'; % model name
opts.learningRate     = [logspace(-3,-3,25) logspace(-4,-4,25)];% you can change the learning rate
